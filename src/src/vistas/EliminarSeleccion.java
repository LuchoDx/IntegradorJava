    private void tablaEliminarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEliminarMousePressed
        //obtenemos la posicion seleccionada en la tabla y el nombre del equipo respectivo.
        int columna = this.tablaEliminar.getSelectedRow();

        seleccionAElimanar = tablaEliminar.getValueAt(columna, 0).toString();
        labelSeleccion.setText(tablaEliminar.getValueAt(columna, 0).toString());

        botonEliminar.setEnabled(true);
    }//GEN-LAST:event_tablaEliminarMousePressed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        //Restringimos que se pueda eliminar al equipo anfitrion.
        if (seleccionAElimanar.equals("argentina")) {
            JOptionPane.showMessageDialog(null, "No puedes eliminar al equipo anfitrion");
        } else {
            //Confirmacion o cancelacion de accion eliminar equipo.
            int opcion = JOptionPane.showConfirmDialog(null, "Esta seguro que desea eliminar al equipo de " + seleccionAElimanar + "?", "Est√°s Seguro?", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

            if (opcion == 0) {
                //se elimina el equipo seleccionado
                EliminarSeleccionado eliminarseleccion = new EliminarSeleccionado(seleccionAElimanar);
                eliminarseleccion.Eliminar();
                LlenarTabla();
                labelSeleccion.setText("");
                botonEliminar.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Equipo Eliminado Correctamente");
            }
            if (opcion == 1) {
                JOptionPane.showMessageDialog(null, "Operacion Cancelada");
                botonEliminar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

