    private void botonSeguirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSeguirActionPerformed
        nombreDelEquipo = txtNombreEquipo.getText().toLowerCase();
        ValidarSiExisteElEquipo validarEquipo = new ValidarSiExisteElEquipo();

        if (nombreDelEquipo.equals("")) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un nombre");
        } else {
            validarEquipo.validar(nombreDelEquipo, this);
        }
    }//GEN-LAST:event_botonSeguirActionPerformed

